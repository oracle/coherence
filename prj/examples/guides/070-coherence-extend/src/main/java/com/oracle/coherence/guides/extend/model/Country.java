package com.oracle.coherence.guides.extend.model;

import java.io.Serializable;

/**
 * Domain class, representing a country, which is used for the demo data.
 *
 * @author Gunnar Hillert  2022.02.25
 */
public class Country implements Serializable {

    private String name;
    private String capital;
    private double population;

    public Country() {
    }

    public Country(String name, String capital, double population) {
        this.name = name;
        this.capital = capital;
        this.population = population;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCapital() {
        return capital;
    }

    public void setCapital(String capital) {
        this.capital = capital;
    }

    public double getPopulation() {
        return population;
    }

    public void setPopulation(double population) {
        this.population = population;
    }

    @Override
    public String toString() {
        return "Country{" +
                "name='" + name + '\'' +
                ", capital='" + capital + '\'' +
                ", population=" + population +
                '}';
    }
}
